class BodyPercentage:
    def __init__(self):
        self.__age = 'easy'
        self.__weight = ''
        self.__height = ''
        self.__waist = ''
        self.__hip = ''
        self.__neck = ''


    def get_age(self):
        return self.__age

    def get_weight(self):
        return self.__weight

    def get_height(self):
        return self.__height

    def set_height(self, height):
        self.__height = height

    def get_waist(self):
        return self.__waist

    def set_waist(self, waist):
        self.__waist = waist

    def get_hip(self):
         return self.__hip

    def set_hip(self, hip):
        self.__hip = hip

    def get_neck(self):
        return self.__neck

    def set_neck(self, neck):
        self.__neck = neck



